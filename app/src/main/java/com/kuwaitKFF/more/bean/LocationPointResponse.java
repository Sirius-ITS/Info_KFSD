package com.kuwaitKFF.more.bean;

public class LocationPointResponse {
	String LocationCode, LocationNameAr, LocationNameEn, LocationGovCode,
		LocationLongitude, LocationLatitudes, latLong, LocationContactAr, LocationContactEn;

	public String getLocationCode() {
		return LocationCode;
	}

	public void setLocationCode(String locationCode) {
		LocationCode = locationCode;
	}

	public String getLocationNameAr() {
		return LocationNameAr;
	}

	public void setLocationNameAr(String locationNameAr) {
		LocationNameAr = locationNameAr;
	}

	public String getLocationNameEn() {
		return LocationNameEn;
	}

	public void setLocationNameEn(String locationNameEn) {
		LocationNameEn = locationNameEn;
	}

	public String getLocationGovCode() {
		return LocationGovCode;
	}

	public void setLocationGovCode(String locationGovCode) {
		LocationGovCode = locationGovCode;
	}

	public String getLocationLongitude() {
		return LocationLongitude;
	}

	public void setLocationLongitude(String locationLongitude) {
		LocationLongitude = locationLongitude;
	}

	public String getLocationLatitudes() {
		return LocationLatitudes;
	}

	public void setLocationLatitudes(String locationLatitudes) {
		LocationLatitudes = locationLatitudes;
	}

	public String getLatLong() {
		return latLong;
	}

	public void setLatLong(String latLong) {
		this.latLong = latLong;
	}


	public String getLocationContactAr() {
		return LocationContactAr;
	}

	public void setLocationContactAr(String locationContactAr) {
		LocationContactAr = locationContactAr;
	}

	public String getLocationContactEn() {
		return LocationContactEn;
	}

	public void setLocationContactEn(String locationContactEn) {
		LocationContactEn = locationContactEn;
	}
}
