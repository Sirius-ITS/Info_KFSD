package com.kuwaitKFF.advertisements;

public class AdvertisementResponseBean {
	
	String AdvertisementSerial, AdvertisementTypeCd, AdvertisementSubject, AdvertisementFileFullPath, AdvertisementFileExtension, AdvertisementLangFlag, AdvertisementDate;

	public String getAdvertisementSerial() {
		return AdvertisementSerial;
	}

	public void setAdvertisementSerial(String advertisementSerial) {
		AdvertisementSerial = advertisementSerial;
	}

	public String getAdvertisementTypeCd() {
		return AdvertisementTypeCd;
	}

	public void setAdvertisementTypeCd(String advertisementTypeCd) {
		AdvertisementTypeCd = advertisementTypeCd;
	}

	public String getAdvertisementSubject() {
		return AdvertisementSubject;
	}

	public void setAdvertisementSubject(String advertisementSubject) {
		AdvertisementSubject = advertisementSubject;
	}

	public String getAdvertisementFileFullPath() {
		return AdvertisementFileFullPath;
	}

	public void setAdvertisementFileFullPath(String advertisementFileFullPath) {
		AdvertisementFileFullPath = advertisementFileFullPath;
	}

	public String getAdvertisementFileExtension() {
		return AdvertisementFileExtension;
	}

	public void setAdvertisementFileExtension(String advertisementFileExtension) {
		AdvertisementFileExtension = advertisementFileExtension;
	}

	public String getAdvertisementLangFlag() {
		return AdvertisementLangFlag;
	}

	public void setAdvertisementLangFlag(String advertisementLangFlag) {
		AdvertisementLangFlag = advertisementLangFlag;
	}

	public void setAdvertisementDate(String advertisementDate) {
		AdvertisementDate = advertisementDate;
	}

	public String getAdvertisementDate() {
		return AdvertisementDate;
	}



}
